print("=== โปรแกรมคำนวณพื้นที่อัจฉริยะ ===")
print("1. สี่เหลี่ยมผืนผ้า")
print("2. สามเหลี่ยม")
print("3. วงกลม")
ตัวเลือก = input("เลือกรูปทรงที่ต้องการคำนวณ (1-3): ")

if ตัวเลือก == "1":
    กว้าง = float(input("กรอกความกว้าง: "))
    ยาว = float(input("กรอกความยาว: "))
    พื้นที่ = กว้าง * ยาว
    print(f"พื้นที่สี่เหลี่ยมคือ: {พื้นที่}")

elif ตัวเลือก == "2":
    ฐาน = float(input("กรอกความยาวฐาน: "))
    สูง = float(input("กรอกความสูง: "))
    พื้นที่ = 0.5 * ฐาน * สูง
    print(f"พื้นที่สามเหลี่ยมคือ: {พื้นที่}")

elif ตัวเลือก == "3":
    รัศมี = float(input("กรอกรัศมีของวงกลม (r): "))
    พื้นที่ = math.pi * (รัศมี ** 2)
    print(f"พื้นที่วงกลมคือ: {พื้นที่:.2f}") # แสดงทศนิยม 2 ตำแหน่ง

else:
    print("คุณเลือกเมนูไม่ถูกต้อง กรุณารันโปรแกรมใหม่อีกครั้ง")
